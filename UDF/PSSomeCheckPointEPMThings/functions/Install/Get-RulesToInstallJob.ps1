function Get-RulesToInstallJob {
    Param(
        [object]$EPSAPI
    )
    Begin {
        $oEPSAPI = if ($EPSAPI) { $EPSAPI } else { $Global:EPSAPI }
        $sOperationName = "getRulesToInstallJob"
        $sQuery = "query getRulesToInstallJob {
            getRulesToInstallJob
          }
          "
        $hVariables = @{}
    }
    Process {
        $oAPIResult = $oEPSAPI.CallAPI($sOperationName, $sQuery, $hVariables)
        return $oAPIResult.data.getRulesToInstallJob
    }
}
